     * Add translations
     *
     * @param \Admin\MedicalBundle\Entity\Staticarticletranslation $translations
     * @return StaticArticles
     */
    public function addTranslation(\Admin\MedicalBundle\Entity\Staticarticletranslation $translations)
    {
        $this->translations[] = $translations;
		$translations->setObject($this);
        return $this;
    }

    /**
     * Remove translations
     *
     * @param \Admin\MedicalBundle\Entity\Staticarticletranslation $translations
     */
    public function removeTranslation(\Admin\MedicalBundle\Entity\Staticarticletranslation $translations)
    {
        $this->translations->removeElement($translations);
    }

    /**
     * Get translations
     *
     * @return \Doctrine\Common\Collections\Collection 
     */
    public function getTranslations()
    {
        return $this->translations;
    }
